def add(a, b):
    result = a + b
    return result

# Case 1: add(1, 2)
result_case1 = add(1, 2)
print("Correct output - case: add(1, 2):", result_case1)

# Case 2: add(100, -2)
result_case2 = add(100, -2)
print("Correct output - case: add(100, -2):", result_case2)

# Case 3: add(-100, -2)
result_case3 = add(-100, -2)
print("Correct output - case: add(-100, -2):", result_case3)

# Case 4: add(0, 0)
result_case4 = add(0, 0)
